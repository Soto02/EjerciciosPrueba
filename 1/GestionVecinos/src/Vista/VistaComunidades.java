    private void limpiarTxtComunidad() {
        txtCodigoComunidad.setText("");
        txtCiudad.setText("");
        txtNombreComunidad.setText("");
    }
    
    private boolean validarTxtcomunidad() {
        if(txtCodigoComunidad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el codigo de la comunidad.");
            return false;
        } else if (txtCiudad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca la ciudad de la comunidad.");
            return false;
        } else if (txtNombreComunidad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el nombre de la comunidad.");
            return false;
        }
        
        return true;
    }
    
    private void limpiarTxtPiso() {
        txtCodigoPiso.setText("");
        txtPiso.setText("");
        txtLetra.setText("");
        txtPropietario.setText("");
        txtTarifa.setText("");
    }
    
    private boolean validarTxtPiso() {
        if(txtCodigoPiso.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el codigo del piso.");
            return false;
        } else if (txtPiso.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el piso.");
            return false;
        } else if (txtLetra.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca la letra del piso.");
            return false;
        } else if (txtPropietario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca el nombre del propietario del piso.");
            return false;
        } else if (txtTarifa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Introduzca la tarifa del piso.");
            return false;
        }
        return true;
    }
    
