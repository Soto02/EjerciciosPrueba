    private void btnAñadirComunidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirComunidadActionPerformed
        boolean validar = validarTxtcomunidad();
        
        if(validar == true) {
            Comunidad comunidad = new Comunidad();
            
            comunidad.setCodigoComunidad(Integer.parseInt(txtCodigoComunidad.getText()));
            comunidad.setCiudad(txtCiudad.getText());
            comunidad.setNombre(txtNombreComunidad.getText());
            
            DAOComunidad dao = new DAOComunidad();
            boolean respuesta = dao.insertarComunidad(comunidad);
            
            if (respuesta == true) {
                    JOptionPane.showMessageDialog(null, "Se añadió correctamente.");
                    limpiarTxtComunidad();
                } else {
                    JOptionPane.showMessageDialog(null, "Hubo un error al añadir la comunidad.");
                }
        }
    }//GEN-LAST:event_btnAñadirComunidadActionPerformed

    private void btnAñadirPropietarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirPropietarioActionPerformed
        boolean validar = validarTxtPiso();
        
        if(validar == true) {
            Piso piso = new Piso();
            
            piso.setCodigoPiso(Integer.parseInt(txtCodigoPiso.getText()));
            piso.setPiso(Integer.parseInt(txtPiso.getText()));
            piso.setLetra(txtLetra.getText());
            piso.setPropietario(txtPropietario.getText());
            piso.setTarifa(Integer.parseInt(txtTarifa.getText()));
            
            DAOPiso dao = new DAOPiso();
            boolean respuesta = dao.insertarPropietario(piso);
            
            if(respuesta == true) {
                JOptionPane.showMessageDialog(null, "Se añadió correctamente.");
                limpiarTxtPiso();
            } else {
                JOptionPane.showMessageDialog(null, "Hubo un error al añadir el piso.");
            }
        }
        
    }//GEN-LAST:event_btnAñadirPropietarioActionPerformed

    private void btnEliminarComunidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarComunidadActionPerformed
        int fila = tablaComunidad.getSelectedRow();
        
        if(fila!= -1) {
            String codigo = tablaComunidad.getValueAt(fila, 0).toString();
            
            int respuesta = JOptionPane.showConfirmDialog(null, "¿Desae borrar la comunidad?", "Advertencia", JOptionPane.WARNING_MESSAGE);
            
            if(respuesta == 0) {
                ControllerComunidad comunidad = new ControllerComunidad();
                
                boolean respt = comunidad.eliminarComunidad(codigo);
                if (respt) {
                    JOptionPane.showMessageDialog(null, "Se elimino correctamente.", "Exito", JOptionPane.INFORMATION_MESSAGE);
                    limpiarTxtComunidad();
                } else {
                    JOptionPane.showMessageDialog(null, "Hubo un error, no se pudo eliminar.", "Error", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Tiene que seleccionar una comunidad de la tabla.");
        }
    }//GEN-LAST:event_btnEliminarComunidadActionPerformed

